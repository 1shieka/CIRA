"""Deterministic evidence-item registry derived from EVALUATION.md.

The verifier LLM call returns category names and evidence-item labels as free
text, which can vary slightly in phrasing between turns (e.g. "UTR Number"
vs "UTR number"). The PDF generator needs stable item_id keys that don't
shift between runs. This module is the single source of truth for that
mapping, kept in code (not regenerated by the model) so the case file does
not silently lose fields due to LLM phrasing drift.

If EVALUATION.md is edited (categories/items added or renamed), this file
must be updated to match. There is a self-check at the bottom of this file
(see `verify_registry_matches_evaluation_md`) that agent.py can call at
startup to catch drift early instead of failing silently at PDF time.
"""

from __future__ import annotations

from dataclasses import dataclass, field


@dataclass(frozen=True)
class EvidenceItem:
    item_id: str
    label: str


@dataclass(frozen=True)
class Category:
    category_id: str
    display_name: str
    required: tuple[EvidenceItem, ...]
    optional: tuple[EvidenceItem, ...] = field(default_factory=tuple)
    critical_flags: tuple[str, ...] = field(default_factory=tuple)
    # item_ids that are conditionally excluded from the denominator
    # (e.g. "Transaction history" for Remote Access Scam if no money was lost)
    conditional_item_ids: tuple[str, ...] = field(default_factory=tuple)


def _items(*pairs: tuple[str, str]) -> tuple[EvidenceItem, ...]:
    return tuple(EvidenceItem(item_id=i, label=l) for i, l in pairs)


