# Map common label variants the verifier LLM might emit back to category_id.
# Used as a fallback if the LLM doesn't return our exact category_id strings.
DISPLAY_NAME_TO_ID = {cat.display_name.lower(): cid for cid, cat in CATEGORIES.items()}


def resolve_category_id(name: str) -> str | None:
    """Best-effort match of a free-text category name to a registry category_id."""
    if not name:
        return None
    key = name.strip().lower()
    if key in CATEGORIES:
        return key
    if key in DISPLAY_NAME_TO_ID:
        return DISPLAY_NAME_TO_ID[key]
    # loose contains-match fallback (e.g. "UPI fraud case" -> upi_fraud)
    for display, cid in DISPLAY_NAME_TO_ID.items():
        if display in key or key in display:
            return cid
    return None


def resolve_item_id(category_id: str, label: str) -> str | None:
    """Best-effort match of a free-text evidence label to a registry item_id."""
    category = CATEGORIES.get(category_id)
    if not category or not label:
        return None
    key = label.strip().lower()
    for item in (*category.required, *category.optional):
        if item.label.lower() == key:
            return item.item_id
        if item.label.lower().split(" (")[0] in key or key in item.label.lower():
            return item.item_id
    return None
